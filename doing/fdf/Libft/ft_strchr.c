#include <libft.h>

char		*ft_strchr(const char *s1, int c)
{
	int i;

	i = 0;
	while (s1[i] && s1[i] != c)
		i++;
	if (s1[i] == c)
		return ((char*)&s1[i]);
	return (NULL);
}
